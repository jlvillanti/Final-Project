def parse_salary(s):
    if pd.isna(s): return None  # Handle missing values appropriately
    if '-' in s:
        s = s.replace(',', '').replace('$', '')
        low, high = s.split('-')
        return (int(low) + int(high)) / 2
    elif 'less' in s.lower():
        return 5000  # Assuming a value for "less than..."
